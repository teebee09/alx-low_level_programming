/**
 * op_add - fnc that adds two integer
 * @a: first integer
 * @b: second integer
 * Return: return sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - fncs use to subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: return differnce
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - fncs that multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: return multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - fncs that divides two integer
 * @a: first integer
 * @b: second integer
 * Return: returns division result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - fncs prints the remainder/modulus two int
 * @a: first integer
 * @b: second integer
 * Return: return modulus result
 */

int op_mod(int a, int b)
{
	return (a % b);
}






































